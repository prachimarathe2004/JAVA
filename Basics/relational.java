public class relational {
    public static void main(String[] args) {
        int x = 6;
        int y = 6;

        boolean result = (x == y);
        System.out.println(result);  // true

        boolean result1 = (x != y);
        System.out.println(result1); // false

        double s = 8.8;
        double p = 9.8;
        boolean result3 = (s <= p);
        System.out.println(result3); // true
    }
}
